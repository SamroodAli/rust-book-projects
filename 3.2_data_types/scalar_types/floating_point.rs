fn main() {
    // rust has two signed floating point data types
    // defaults to f64, the other is f32
    let floating_point_64: f64 = 10.22;

    let floating_point_32: f32 = 10.44;

    println!("The value of floating_point_64 is {floating_point_64}");
    println!("The value of floating_point_32 is {floating_point_32}");
}
